FOR EACH ROW
BEGIN
    :NEW.id_cita := seq_cita.NEXTVAL;
END;
/
CREATE OR REPLACE TRIGGER trg_factura
BEFORE INSERT ON FACTURACION
FOR EACH ROW
BEGIN
    :NEW.id_factura := seq_factura.NEXTVAL;
END;
/
CREATE OR REPLACE TRIGGER trg_historial
BEFORE INSERT ON HISTORIAL_MEDICO
FOR EACH ROW
BEGIN
    :NEW.id_historial := seq_historial.NEXTVAL;
END;
/
CREATE OR REPLACE FUNCTION fn_total_pacientes RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM PACIENTE; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_total_medicos RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM MEDICO; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_total_citas RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM CITA; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_total_facturas RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM FACTURACION; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_salario_total RETURN NUMBER IS v NUMBER;
BEGIN SELECT SUM(salario) INTO v FROM MEDICO; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_promedio_salario RETURN NUMBER IS v NUMBER;
BEGIN SELECT AVG(salario) INTO v FROM MEDICO; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_citas_hoy RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM CITA WHERE TRUNC(fecha_cita)=TRUNC(SYSDATE); RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_facturas_pendientes RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM FACTURACION WHERE estado_pago='PENDIENTE'; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_facturas_pagadas RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM FACTURACION WHERE estado_pago='PAGADO'; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_max_factura RETURN NUMBER IS v NUMBER;
BEGIN SELECT MAX(monto_total) INTO v FROM FACTURACION; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_min_factura RETURN NUMBER IS v NUMBER;
BEGIN SELECT MIN(monto_total) INTO v FROM FACTURACION; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_total_historial RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM HISTORIAL_MEDICO; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_citas_activas RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM CITA WHERE estado='ACTIVA'; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_citas_canceladas RETURN NUMBER IS v NUMBER;
BEGIN SELECT COUNT(*) INTO v FROM CITA WHERE estado='CANCELADA'; RETURN v; END;
/
CREATE OR REPLACE FUNCTION fn_total_ingresos RETURN NUMBER IS v NUMBER;
BEGIN SELECT SUM(monto_total) INTO v FROM FACTURACION; RETURN v; END;
/
CREATE OR REPLACE VIEW vw_pacientes AS SELECT * FROM PACIENTE;
