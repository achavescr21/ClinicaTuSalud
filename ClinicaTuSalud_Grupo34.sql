SET SERVEROUTPUT ON;

CREATE SEQUENCE seq_paciente START WITH 1;
CREATE SEQUENCE seq_medico START WITH 1;
CREATE SEQUENCE seq_cita START WITH 1;
CREATE SEQUENCE seq_factura START WITH 1;
CREATE SEQUENCE seq_historial START WITH 1;

CREATE TABLE PACIENTE(
    id_paciente NUMBER PRIMARY KEY,
    nombre VARCHAR2(50),
    apellido VARCHAR2(50),
    telefono VARCHAR2(20),
    correo VARCHAR2(100),
    fecha_registro DATE
);
CREATE TABLE MEDICO(
    id_medico NUMBER PRIMARY KEY,
    nombre VARCHAR2(50),
    especialidad VARCHAR2(50),
    telefono VARCHAR2(20),
    salario NUMBER
);

CREATE TABLE CITA(
    id_cita NUMBER PRIMARY KEY,
    id_paciente NUMBER,
    id_medico NUMBER,
    fecha_cita DATE,
    estado VARCHAR2(20)
);
CREATE TABLE FACTURACION(
    id_factura NUMBER PRIMARY KEY,
    id_paciente NUMBER,
    monto_total NUMBER,
    estado_pago VARCHAR2(20)
);
CREATE TABLE HISTORIAL_MEDICO(
    id_historial NUMBER PRIMARY KEY,
    id_paciente NUMBER,
    fecha DATE,
    diagnostico VARCHAR2(200),
    tratamiento VARCHAR2(200),
    observaciones VARCHAR2(200)
);
CREATE OR REPLACE TRIGGER trg_paciente
BEFORE INSERT ON PACIENTE
FOR EACH ROW
BEGIN
    :NEW.id_paciente := seq_paciente.NEXTVAL;
END;
/
CREATE OR REPLACE TRIGGER trg_medico
