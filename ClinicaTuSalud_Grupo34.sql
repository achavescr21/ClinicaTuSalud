CREATE OR REPLACE VIEW vw_facturacion AS SELECT * FROM FACTURACION;
CREATE OR REPLACE VIEW vw_historial AS SELECT * FROM HISTORIAL_MEDICO;
CREATE OR REPLACE VIEW vw_ingresos AS
SELECT SUM(monto_total) total FROM FACTURACION;
CREATE OR REPLACE VIEW vw_citas_activas AS
SELECT * FROM CITA WHERE estado='ACTIVA';
CREATE OR REPLACE VIEW vw_citas_canceladas AS
SELECT * FROM CITA WHERE estado='CANCELADA';
CREATE OR REPLACE VIEW vw_medicos_especialidad AS
SELECT nombre, especialidad FROM MEDICO;
CREATE OR REPLACE VIEW vw_pacientes_recientes AS
SELECT * FROM PACIENTE WHERE fecha_registro > SYSDATE-30;

CREATE OR REPLACE PROCEDURE sp_insert_paciente(n VARCHAR2,a VARCHAR2,t VARCHAR2,c VARCHAR2)
IS BEGIN INSERT INTO PACIENTE VALUES(NULL,n,a,t,c,SYSDATE); END;
/
CREATE OR REPLACE PROCEDURE sp_update_paciente(id NUMBER,n VARCHAR2)
IS BEGIN UPDATE PACIENTE SET nombre=n WHERE id_paciente=id; END;
/
CREATE OR REPLACE PROCEDURE sp_delete_paciente(id NUMBER)
IS BEGIN DELETE FROM PACIENTE WHERE id_paciente=id; END;
/
CREATE OR REPLACE PROCEDURE sp_insert_medico(n VARCHAR2,e VARCHAR2,t VARCHAR2,s NUMBER)
IS BEGIN INSERT INTO MEDICO VALUES(NULL,n,e,t,s); END;
/
