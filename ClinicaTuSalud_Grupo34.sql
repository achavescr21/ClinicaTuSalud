IS BEGIN UPDATE MEDICO SET salario=s WHERE id_medico=id; END;
/
CREATE OR REPLACE PROCEDURE sp_delete_medico(id NUMBER)
IS BEGIN DELETE FROM MEDICO WHERE id_medico=id; END;
/
CREATE OR REPLACE PROCEDURE sp_insert_cita(p NUMBER,m NUMBER,f DATE,e VARCHAR2)
IS BEGIN INSERT INTO CITA VALUES(NULL,p,m,f,e); END;
/
CREATE OR REPLACE PROCEDURE sp_delete_cita(p NUMBER)
IS BEGIN DELETE FROM CITA WHERE id_cita=p; END;
/
CREATE OR REPLACE PROCEDURE sp_update_cita_estado(p NUMBER,e VARCHAR2)
IS BEGIN UPDATE CITA SET estado=e WHERE id_cita=p; END;
/
CREATE OR REPLACE PROCEDURE sp_insert_factura(p NUMBER,m NUMBER,e VARCHAR2)
IS BEGIN INSERT INTO FACTURACION VALUES(NULL,p,m,e); END;
/
CREATE OR REPLACE PROCEDURE sp_update_factura(p NUMBER,m NUMBER)
IS BEGIN UPDATE FACTURACION SET monto_total=m WHERE id_factura=p; END;
/
CREATE OR REPLACE PROCEDURE sp_delete_factura(p NUMBER)
IS BEGIN DELETE FROM FACTURACION WHERE id_factura=p; END;
/
CREATE OR REPLACE PROCEDURE sp_insert_historial(p NUMBER,d VARCHAR2,t VARCHAR2,o VARCHAR2)
IS BEGIN INSERT INTO HISTORIAL_MEDICO VALUES(NULL,p,SYSDATE,d,t,o); END;
/
CREATE OR REPLACE PROCEDURE sp_aumentar_salarios(p NUMBER)
IS BEGIN UPDATE MEDICO SET salario=salario+p; END;
/
CREATE OR REPLACE PROCEDURE sp_reset_estado_citas
IS BEGIN UPDATE CITA SET estado='ACTIVA'; END;
